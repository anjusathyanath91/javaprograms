package Assignments;

public class Addition {
    protected int num1;
    protected int num2;

    // Constructor to initialize numbers.
    public Addition(int num1, int num2) {
        this.num1 = num1;
        this.num2 = num2;
    }

    // Method to return the addition result of two numbers
    public int getAdditionResult() {
        return num1 + num2;
    }
    
    
    
}
